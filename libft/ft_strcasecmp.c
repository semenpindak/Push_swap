#include "libft.h"

int	ft_strcasecmp(const char *s1, const char *s2)
{
	while ((*s1 - *s2 == 0 || *s1 - *s2 == 32 || *s1 - *s2 == -32)
				&& *s1 != '\0' && *s2 != '\0')
	{
		s1++;
		s2++;
	}
	return ((int)(*(unsigned char *)s1 - *(unsigned char *)s2));
}
